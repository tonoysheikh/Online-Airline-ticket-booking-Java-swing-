    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
            String pname = jTextField1.getText();
            String username = jTextField2.getText();
            String contact = jTextField3.getText();
            String address = jTextField4.getText();
            String email = jTextField5.getText();
            String cp = jTextField6.getText();
            String passport = jTextField7.getText();
            String nationality = jTextField8.getText();
            

        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/onlineair" , "root" , "123456");
            
            String k = "INSERT INTO registration VALUES(?,?,?,?,?,?,?,?)";
            PreparedStatement ps = con.prepareStatement(k);
            
            ps.setString(1, pname);
            ps.setString(2, username);
            ps.setString(3, contact);
            ps.setString(4, address);
            ps.setString(5, email);
            ps.setString(6, cp);
            ps.setString(7, passport);
            ps.setString(8, nationality);

            ps.executeUpdate();
            JFrame jf = new JFrame();
            jf.setAlwaysOnTop(true);
            JOptionPane.showMessageDialog(jf, "Registration successfully complete");
            
            setVisible(false);
            new Homepage().setVisible(true);


        }
        catch(Exception e)
        {
            JFrame jf = new JFrame();
            jf.setAlwaysOnTop(true);
            JOptionPane.showMessageDialog(jf, e);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

